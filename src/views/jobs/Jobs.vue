<template>
  <h1>Jobs</h1>
  <div v-for="job in jobs" :key="job.id" class="job">
      <router-link :to="{name: 'JobDetails', params: {id: job.id}}">
          <h2>{{ job.title }}</h2>
      </router-link>
  </div>
</template>
<script>
export default {
    data: () => ({
        jobs: []
    }),
    async mounted(){
       await fetch('http://localhost:3000/jobs').
        then((res) => res.json())
        .then((data) => this.jobs = data)
        .catch(err => console.error(err));
    }
}
</script>

